// Cosine similarity between two vectors. Used by adapters whose keyword
// primitive (BM25, sparse) does not itself return a cosine score, so we
// recompute it from the stored vector to honor the score-is-cosine convention.
export function cosineSimilarity(a: number[], b: number[]): number {
  let dot = 0;
  let na = 0;
  let nb = 0;
  const n = Math.min(a.length, b.length);
  for (let i = 0; i < n; i++) {
    dot += a[i] * b[i];
    na += a[i] * a[i];
    nb += b[i] * b[i];
  }
  if (na === 0 || nb === 0) return 0;
  return dot / (Math.sqrt(na) * Math.sqrt(nb));
}
